void test_fstr8_no_space(void) {
	fprintf(stderr, "----------------------------------------------\n");
	fprintf(stderr, "Running str8_t no space tests\n");
	fprintf(stderr, "----------------------------------------------\n");
	fstr8_init(s, "1234567");

	fstr_write_char(&s, '6');
	fstr_write(&s, (bool)0);
	fstr_write(&s, "string");
	fstr_write(&s, (uint8_t)0);
	fstr_write(&s, (uint16_t)0);
	fstr_write(&s, (uint32_t)0);
	fstr_write(&s, (uint64_t)0);
	fstr_write(&s, (int8_t)0);
	fstr_write(&s, (int16_t)0);
	fstr_write(&s, (int32_t)0);
	fstr_write(&s, (int64_t)0);
	fstr_write_float(&s, (float)0.0, true);
	fstr_write_double(&s, (double)0.0, true);
	fstr_write_hex(&s, (uint8_t)0x0, true);
	fstr_write_hex(&s, (uint16_t)0x0, true);
	fstr_write_hex(&s, (uint32_t)0x0, true);
	fstr_write_hex(&s, (uint64_t)0x0, true);
	fstr_write_xhex(&s, (uint8_t)0x0, true);
	fstr_write_xhex(&s, (uint16_t)0x0, true);
	fstr_write_xhex(&s, (uint32_t)0x0, true);
	fstr_write_xhex(&s, (uint64_t)0x0, true);
	fstr_write_HEX(&s, (uint8_t)0x0, true);
	fstr_write_HEX(&s, (uint16_t)0x0, true);
	fstr_write_HEX(&s, (uint32_t)0x0, true);
	fstr_write_HEX(&s, (uint64_t)0x0, true);
	fstr_write_xHEX(&s, (uint8_t)0x0, true);
	fstr_write_xHEX(&s, (uint16_t)0x0, true);
	fstr_write_xHEX(&s, (uint32_t)0x0, true);
	fstr_write_xHEX(&s, (uint64_t)0x0, true);
}

